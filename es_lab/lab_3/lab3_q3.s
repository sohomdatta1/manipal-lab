Reset_Handler
	LDR R0, =N1
	LDR R1, =N2
	LDR R2, [R0]
	LDR R3, [R1]
	
GCDT
	CMP R2, R3
	SUBLT R3, R3, R2  
	SUBGT R2, R2, R3
	BNE GCDT

STRGCD
	LDR R4, =GCD
	STR R2, [R4]
	
LCMT
	LDR R5, [R0]
	LDR R6, [R1]
	MOV R4, #0
	MUL R7, R5, R6

DIVISION
	SUB R7, R7, R2
	ADD R4, R4, #1
	CMP R7, R2
	BGE DIVISION

STRLCM
	LDR R8, =LCM
	STR R4, [R8]

STOP B STOP
